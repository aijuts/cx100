package com.aijuts.cx100.entity;

public class Vegetable {
	
	private int total;
	
	private VegetableData data;
	
	private boolean success;

	public int getTotal() {
		return total;
	}

	public void setTotal(int total) {
		this.total = total;
	}

	public VegetableData getData() {
		return data;
	}

	public void setData(VegetableData data) {
		this.data = data;
	}

	public boolean isSuccess() {
		return success;
	}

	public void setSuccess(boolean success) {
		this.success = success;
	}

}
