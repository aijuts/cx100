package com.aijuts.cx100.entity;

public class Order {
	
	private String orderid;
	
	private String code;
	
	private String state;
	
	private String addtime;
	
	private String schedule;
	
	private String sellerid;
	
	private String consumerid;
	
	private String name;
	
	private String dir;
	
	private String consuercount;
	
	private String total;
	
	private int commitState;
	
	private String commitInfo;

	public String getOrderid() {
		return orderid;
	}

	public void setOrderid(String orderid) {
		this.orderid = orderid;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getAddtime() {
		return addtime;
	}

	public void setAddtime(String addtime) {
		this.addtime = addtime;
	}

	public String getSchedule() {
		return schedule;
	}

	public void setSchedule(String schedule) {
		this.schedule = schedule;
	}

	public String getSellerid() {
		return sellerid;
	}

	public void setSellerid(String sellerid) {
		this.sellerid = sellerid;
	}

	public String getConsumerid() {
		return consumerid;
	}

	public void setConsumerid(String consumerid) {
		this.consumerid = consumerid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDir() {
		return dir;
	}

	public void setDir(String dir) {
		this.dir = dir;
	}

	public String getConsuercount() {
		return consuercount;
	}

	public void setConsuercount(String consuercount) {
		this.consuercount = consuercount;
	}

	public String getTotal() {
		return total;
	}

	public void setTotal(String total) {
		this.total = total;
	}

	public int getCommitState() {
		return commitState;
	}

	public void setCommitState(int commitState) {
		this.commitState = commitState;
	}

	public String getCommitInfo() {
		return commitInfo;
	}

	public void setCommitInfo(String commitInfo) {
		this.commitInfo = commitInfo;
	}

}
